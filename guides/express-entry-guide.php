                    <h2 style="color: var(--color-burgundy); margin-bottom: 20px;">What is Express Entry?</h2>
                    <p>Express Entry is Canada's online immigration application management system for skilled workers. It was introduced in January 2015 to process applications for permanent residence under these federal economic immigration programs:</p>
                    
                    <ul style="margin: 20px 0; padding-left: 20px;">
                        <li>Federal Skilled Worker Program (FSWP)</li>
                        <li>Federal Skilled Trades Program (FSTP)</li>
                        <li>Canadian Experience Class (CEC)</li>
                    </ul>
                    
                    <p>Through Express Entry, Immigration, Refugees and Citizenship Canada (IRCC) manages the pool of candidates, invites the highest-ranking candidates to apply for permanent residence, and processes their applications.</p>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 1: Check Your Eligibility</h2>
                    <p>Before you start the Express Entry process, determine if you're eligible for one of the above immigration programs. Each program has different requirements for:</p>
                    
                    <ul style="margin: 20px 0; padding-left: 20px;">
                        <li>Skilled work experience</li>
                        <li>Language ability in English and/or French</li>
                        <li>Education</li>
                    </ul>
                    
                    <p>Use our <a href="../assessment-calculator/eligibility-calculator.php">Eligibility Calculator</a> to quickly assess if you qualify.</p>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 2: Prepare Your Documents</h2>
                    <p>To submit an Express Entry profile, you'll need:</p>
                    
                    <ul style="margin: 20px 0; padding-left: 20px;">
                        <li>Passport or travel document</li>
                        <li>Language test results (IELTS, CELPIP for English; TEF for French)</li>
                        <li>Educational Credential Assessment (ECA) if you studied outside Canada</li>
                        <li>Provincial Nomination (if applicable)</li>
                        <li>Proof of funds to settle in Canada (unless you have a valid job offer)</li>
                        <li>Work experience letters from employers</li>
                    </ul>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 3: Create Your Express Entry Profile</h2>
                    <p>Once you have your documents, create an Express Entry profile on the IRCC website. You'll provide information about your:</p>
                    
                    <ul style="margin: 20px 0; padding-left: 20px;">
                        <li>Language skills</li>
                        <li>Education</li>
                        <li>Work experience</li>
                        <li>Family members</li>
                        <li>Other details</li>
                    </ul>
                    
                    <p>Your profile will be valid for 12 months. If you don't receive an invitation to apply within that time, you can submit a new profile.</p>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 4: Receive an Invitation to Apply (ITA)</h2>
                    <p>Candidates in the Express Entry pool are ranked using the Comprehensive Ranking System (CRS). IRCC conducts regular draws, inviting the highest-ranking candidates to apply for permanent residence.</p>
                    
                    <p>Factors that affect your CRS score include:</p>
                    <ul style="margin: 20px 0; padding-left: 20px;">
                        <li>Age</li>
                        <li>Education</li>
                        <li>Language proficiency</li>
                        <li>Work experience</li>
                        <li>Canadian work experience</li>
                        <li>Spouse or common-law partner factors</li>
                        <li>Provincial nomination</li>
                        <li>Job offer from a Canadian employer</li>
                    </ul>
                    
                    <p>Check your potential score with our <a href="../assessment-calculator/crs-score-calculator.php">CRS Score Calculator</a>.</p>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 5: Submit Your Application</h2>
                    <p>If you receive an ITA, you'll have 60 days to submit a complete application for permanent residence, including:</p>
                    
                    <ul style="margin: 20px 0; padding-left: 20px;">
                        <li>Identity documents</li>
                        <li>Medical exam results</li>
                        <li>Police certificates</li>
                        <li>Detailed work history</li>
                        <li>Other supporting documents</li>
                    </ul>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 6: Get Your Permanent Residence</h2>
                    <p>IRCC aims to process complete applications within 6 months. If approved, you'll receive a Confirmation of Permanent Residence (COPR) and a permanent resident visa (if you're from a country that requires a visa).</p>
                    
                    <div style="background-color: #f9f5f1; padding: 20px; border-radius: 8px; margin: 30px 0;">
                        <h3 style="color: var(--color-burgundy); margin-bottom: 15px;">Pro Tips for Express Entry Success</h3>
                        <ul style="padding-left: 20px;">
                            <li>Take language tests before starting your Express Entry profile</li>
                            <li>Get your educational credentials assessed early</li>
                            <li>Improve your CRS score by enhancing your language skills or education</li>
                            <li>Gather work experience documents while you're still employed</li>
                            <li>Keep your profile updated with any changes to your situation</li>
                        </ul>
                    </div>
