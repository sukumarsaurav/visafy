                <div class="content-box" style="background: white; border-radius: 10px; padding: 40px; box-shadow: 0 5px 15px rgba(0,0,0,0.05); margin-bottom: 30px;">
                    <h2 style="color: var(--color-burgundy); margin-bottom: 20px;">Understanding Study Permits</h2>
                    <p>A study permit is a document issued by Immigration, Refugees and Citizenship Canada (IRCC) that allows foreign nationals to study at designated learning institutions (DLIs) in Canada. If you plan to study in Canada for a program longer than six months, you'll need a study permit.</p>
                    
                    <div style="margin: 25px 0; padding: 15px; background-color: #f9f5f1; border-left: 4px solid var(--color-burgundy); border-radius: 4px;">
                        <strong>Important:</strong> A study permit is not a visa. You may also need a visitor visa or an electronic travel authorization (eTA) to enter Canada, depending on your citizenship.
                    </div>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 1: Determine Your Eligibility</h2>
                    <p>To be eligible for a study permit, you must:</p>
                    
                    <ul style="margin: 20px 0; padding-left: 20px;">
                        <li>Be accepted by a designated learning institution (DLI) in Canada</li>
                        <li>Prove you have enough money to pay tuition fees, living expenses, and return transportation</li>
                        <li>Have no criminal record and be willing to provide a police certificate if required</li>
                        <li>Be in good health and willing to complete a medical examination if required</li>
                        <li>Satisfy an immigration officer that you will leave Canada at the end of your authorized stay</li>
                    </ul>
                    
                    <p>Use our <a href="../assessment-calculator/study-permit-checker.php">Study Permit Checker</a> to see if you meet the basic requirements.</p>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 2: Receive an Acceptance Letter</h2>
                    <p>Before applying for a study permit, you must be accepted by a Canadian designated learning institution (DLI). Once accepted, you'll receive an acceptance letter, which is required for your study permit application.</p>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 3: Prepare Your Documents</h2>
                    <p>You'll need to submit several documents with your study permit application:</p>
                    
                    <ul style="margin: 20px 0; padding-left: 20px;">
                        <li><strong>Acceptance letter</strong> from your designated learning institution</li>
                        <li><strong>Proof of financial support</strong> showing you can support yourself and any family members coming with you to Canada</li>
                        <li><strong>Valid passport or travel document</strong></li>
                        <li><strong>Passport-sized photographs</strong> meeting specific requirements</li>
                        <li><strong>Language test results</strong> if applicable</li>
                        <li><strong>Statement of purpose/Letter of explanation</strong> explaining why you want to study in Canada</li>
                        <li><strong>Proof of upfront medical exam</strong> if required based on your country</li>
                        <li><strong>Police certificates</strong> if required</li>
                    </ul>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 4: Prepare Proof of Financial Support</h2>
                    <p>You must prove you have enough money to cover:</p>
                    
                    <ul style="margin: 20px 0; padding-left: 20px;">
                        <li>Tuition fees (as indicated on your acceptance letter)</li>
                        <li>Living expenses for yourself and any family members accompanying you</li>
                        <li>Return transportation for yourself and any family members</li>
                    </ul>
                    
                    <p>As of 2023, IRCC requires you to show approximately CAD $10,000 per year (or CAD $833 per month) for living expenses, in addition to your tuition fees. This amount increases if family members are accompanying you.</p>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 5: Submit Your Application</h2>
                    <p>You can apply for a study permit online or on paper. However, online applications are generally processed faster. To apply online:</p>
                    
                    <ol style="margin: 20px 0; padding-left: 20px;">
                        <li>Create an IRCC account</li>
                        <li>Fill out the application forms</li>
                        <li>Pay the application fee (CAD $150)</li>
                        <li>Upload all required documents</li>
                        <li>Submit your application</li>
                    </ol>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 6: Attend a Biometrics Appointment (if required)</h2>
                    <p>Most applicants need to give biometrics (fingerprints and a photo) as part of their application. If you need to give biometrics, you'll receive instructions after submitting your application.</p>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 7: Attend an Interview (if required)</h2>
                    <p>In some cases, you may be asked to attend an interview at your local visa office. The interview helps the visa officer assess your application further.</p>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 8: Receive Your Study Permit</h2>
                    <p>If your application is approved, you'll receive a letter of introduction confirming the approval. This is not your study permit. You'll receive the actual study permit when you arrive in Canada.</p>
                    
                    <p>When you arrive in Canada, present the following to the border services officer:</p>
                    <ul style="margin: 20px 0; padding-left: 20px;">
                        <li>Your passport or travel document</li>
                        <li>Your letter of introduction</li>
                        <li>A copy of your acceptance letter</li>
                        <li>Proof of financial support</li>
                        <li>Any other documents recommended in your letter of introduction</li>
                    </ul>
                    
                    <div style="background-color: #f9f5f1; padding: 20px; border-radius: 8px; margin: 30px 0;">
                        <h3 style="color: var(--color-burgundy); margin-bottom: 15px;">Pro Tips for Study Permit Success</h3>
                        <ul style="padding-left: 20px;">
                            <li>Apply at least 3-4 months before your program starts</li>
                            <li>Ensure all your documents are clear, complete, and accurate</li>
                            <li>Provide detailed financial documentation showing the source of your funds</li>
                            <li>Demonstrate strong ties to your home country to show you'll return after your studies</li>
                            <li>Be honest in all aspects of your application</li>
                            <li>If you have dependents, include them in your initial application even if they won't accompany you immediately</li>
                        </ul>
                    </div>
