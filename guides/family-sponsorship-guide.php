                <div class="content-box" style="background: white; border-radius: 10px; padding: 40px; box-shadow: 0 5px 15px rgba(0,0,0,0.05); margin-bottom: 30px;">
                    <h2 style="color: var(--color-burgundy); margin-bottom: 20px;">Understanding Family Sponsorship</h2>
                    <p>Family sponsorship is an immigration program that allows Canadian citizens and permanent residents to sponsor eligible family members to become permanent residents of Canada. This program reflects Canada's commitment to keeping families together whenever possible.</p>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Who Can Be a Sponsor?</h2>
                    <p>To sponsor a family member, you must:</p>
                    
                    <ul style="margin: 20px 0; padding-left: 20px;">
                        <li>Be a Canadian citizen, permanent resident, or registered under the Canadian Indian Act</li>
                        <li>Be at least 18 years old</li>
                        <li>Live in Canada (except for Canadian citizens sponsoring a spouse, partner, or child)</li>
                        <li>Meet minimum income requirements (for certain sponsorships)</li>
                        <li>Sign an undertaking to financially support your sponsored relative for a specific period</li>
                        <li>Sign a sponsorship agreement committing to provide financial support</li>
                    </ul>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Who Can Be Sponsored?</h2>
                    <p>You can sponsor the following family members:</p>
                    
                    <h3 style="color: var(--color-burgundy); margin: 20px 0 15px; font-size: 1.3rem;">Spouse, Partner, or Dependent Child</h3>
                    <ul style="margin: 15px 0; padding-left: 20px;">
                        <li>Your spouse or common-law partner</li>
                        <li>Your conjugal partner (under specific circumstances)</li>
                        <li>Your dependent child or adopted child</li>
                    </ul>
                    
                    <h3 style="color: var(--color-burgundy); margin: 20px 0 15px; font-size: 1.3rem;">Parents and Grandparents</h3>
                    <ul style="margin: 15px 0; padding-left: 20px;">
                        <li>Your parents</li>
                        <li>Your grandparents</li>
                    </ul>
                    
                    <h3 style="color: var(--color-burgundy); margin: 20px 0 15px; font-size: 1.3rem;">Other Relatives (under specific circumstances)</h3>
                    <ul style="margin: 15px 0; padding-left: 20px;">
                        <li>Orphaned brother, sister, nephew, niece, or grandchild who is under 18 years of age</li>
                        <li>Other relatives, if you have no living relatives as listed above</li>
                    </ul>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 1: Determine Eligibility</h2>
                    <p>Before starting the application process, confirm that both you and your family member meet the eligibility requirements for sponsorship. Some factors that could affect eligibility include:</p>
                    
                    <ul style="margin: 20px 0; padding-left: 20px;">
                        <li>Immigration status in Canada</li>
                        <li>Financial capability to support sponsored relatives</li>
                        <li>Past criminal convictions</li>
                        <li>Previous sponsorship defaults</li>
                        <li>Receipt of social assistance (not for disability reasons)</li>
                    </ul>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 2: Submit a Sponsorship Application</h2>
                    <p>The family sponsorship process involves two applications:</p>
                    
                    <ol style="margin: 20px 0; padding-left: 20px;">
                        <li><strong>Sponsorship Application:</strong> This is completed by the Canadian citizen or permanent resident who wants to sponsor their family member.</li>
                        <li><strong>Permanent Residence Application:</strong> This is completed by the family member being sponsored.</li>
                    </ol>
                    
                    <p>Both applications are usually submitted together in one package to Immigration, Refugees and Citizenship Canada (IRCC).</p>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 3: Pay the Fees</h2>
                    <p>The family sponsorship process involves several fees:</p>
                    
                    <ul style="margin: 20px 0; padding-left: 20px;">
                        <li>Sponsorship fee</li>
                        <li>Principal applicant processing fee</li>
                        <li>Right of permanent residence fee</li>
                        <li>Biometrics fee</li>
                        <li>Additional fees for dependent children (if applicable)</li>
                    </ul>
                    
                    <p>The exact fee amounts change periodically, so check the IRCC website for the most current information.</p>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 4: Submit Biometrics</h2>
                    <p>Most applicants need to provide biometrics (fingerprints and a photo) as part of their application. After submitting the application, the sponsored family member will receive a biometrics instruction letter explaining how to provide their biometrics.</p>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 5: Medical Examination</h2>
                    <p>The sponsored family member must undergo a medical examination by a designated panel physician. IRCC will provide instructions on when and how to complete the medical examination.</p>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 6: Additional Documents and Interview</h2>
                    <p>IRCC may request additional documents or ask the sponsored family member to attend an interview at a visa office. It's important to respond promptly to any requests for additional information.</p>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Step 7: Receive a Decision</h2>
                    <p>After reviewing the application, IRCC will make a decision. If approved, the sponsored family member will receive:</p>
                    
                    <ul style="margin: 20px 0; padding-left: 20px;">
                        <li>A Confirmation of Permanent Residence (COPR)</li>
                        <li>A permanent resident visa (if from a country that requires a visa to travel to Canada)</li>
                    </ul>
                    
                    <h2 style="color: var(--color-burgundy); margin: 30px 0 20px;">Special Programs</h2>
                    
                    <h3 style="color: var(--color-burgundy); margin: 20px 0 15px; font-size: 1.3rem;">Spousal Sponsorship</h3>
                    <p>There are two streams for spousal sponsorship:</p>
                    
                    <ul style="margin: 15px 0; padding-left: 20px;">
                        <li><strong>Outland Sponsorship:</strong> For spouses/partners living outside Canada</li>
                        <li><strong>Inland Sponsorship:</strong> For spouses/partners already living in Canada</li>
                    </ul>
                    
                    <h3 style="color: var(--color-burgundy); margin: 20px 0 15px; font-size: 1.3rem;">Parents and Grandparents Program (PGP)</h3>
                    <p>The PGP has limited application intake each year. Alternatives include:</p>
                    
                    <ul style="margin: 15px 0; padding-left: 20px;">
                        <li><strong>Super Visa:</strong> A multiple-entry visa valid for up to 10 years</li>
                        <li><strong>Regular Visitor Visa:</strong> For shorter visits</li>
                    </ul>
                    
                    <div style="background-color: #f9f5f1; padding: 20px; border-radius: 8px; margin: 30px 0;">
                        <h3 style="color: var(--color-burgundy); margin-bottom: 15px;">Pro Tips for Family Sponsorship Success</h3>
                        <ul style="padding-left: 20px;">
                            <li>Start gathering documents early, especially for relationships that need proof</li>
                            <li>Include a detailed cover letter explaining your relationship and sponsorship eligibility</li>
                            <li>For spousal sponsorship, provide substantial evidence of the genuine nature of your relationship</li>
                            <li>Keep copies of all documents submitted</li>
                            <li>Respond promptly to any requests for additional information</li>
                            <li>Be truthful in all aspects of your application</li>
                        </ul>
                    </div>
